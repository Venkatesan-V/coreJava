package com.lao.loopingStatement;

public class ForLoopEx {

	public static void main(String[] args) {
//		forLoop
		for (int i = 0; i<5; i++) {
			System.out.println("I will join as a software tester");
		}
		System.out.println("==========END============");
		
//		whileLoop
		int i=0;
		while(i<5) {
			System.out.println("I will get job ASAP");
			i++;
		}
		System.out.println("==========END============");
		
//		do-WhileLoop
		int j=0;
		do {
			System.out.println("I will get good job");
			j++;
		}while (j<5);
			
			
			
	}
}
